String BusAPIClient::getStopData() {
    int requestTime = millis();
    while(wifiConnected == false) {
        if(millis() - requestTime > 10000) {
            Serial.println("WiFi not connected yet");
            return "";
        }
    }
 
    HTTPClient http;

    http.begin(String("https://metromap.cityofmadison.com/bustime/api/v3/getpredictions?key=")+API_TOKEN+String("&stpid=")+STOP_IDS+String("&rt=A,B,C,D,75"), root_ca); //Specify the URL and certificate
    int httpCode = http.GET();                                                  //Make the request

    if (httpCode > 0) { //Check for the returning code

        String payload = http.getString();
        Serial.println(httpCode);
        Serial.println(payload);
        return payload;
    }

    else {
      Serial.print("Error on HTTP request: ");
      Serial.println(httpCode);
    }

    http.end(); //Free the resources

  return "";
}